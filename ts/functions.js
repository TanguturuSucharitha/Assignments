function add(n1, n2) {
    var result;
    result = n1 + n2;
    return result;
}
console.log(add(10, 20));
function add(n1, n2, n3) {
    var result;
    result = n1 + n2 + n3;
    return result;
}
console.log(add(10, 20, 30));
function diff(n1, n2) {
    var result;
    result = n1 - n2;
    return result;
}
console.log(diff(10, 20));
